'''Retorna Valores bool = True ou False'''

saldo = 450
saque = 200
print(saldo == saque) # Verifica se saldo é igual a saque
print(saldo != saque) # '' ''  ''  '' é diferente a saque
print(saldo > saque) # Saldo é maior que saque
print(saldo < saque) # Saldo menor que saque
print(saldo <= saque) # Saldo menor ou igual a saque
print(saldo >= saque) # Saldo maior ou igual a saque
